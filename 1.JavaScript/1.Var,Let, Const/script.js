// Var always changeable.
var x= "raja";
console.log(x);
x = "prabhat";
console.log(x);
// If target any value . Any time you change that Value.

// Let always Changeable. 
let y = 10;
console.log(y);
y=20;
console.log(y);
// If target any value. Any time you change that value.

// Const not changeable.
const a = 50;
console.log(a);
a=100;
console.log(a);
// If target any value. Can not change it any time.